    /**
     * @param string $key
     * @param array $replaces
     * @return mixed|string
     */
    public static function cbd(string $key, array $replaces = [], bool $default = false)  {
        static $trans = null;

        if ($trans === null) {
            $trans = array_dot(self::cbd);
        }

        $result = self::getData($trans, $key, $replaces);

        if(!$result && $default){
            return $trans['unknown'];
        }

        return $result;
    }

    /**
     * @param string $key
     * @param array $replaces
     * @return mixed|string
     */
    public static function trans(string $key, array $replaces = [])  {
        static $trans = null;

        if ($trans === null) {
            $trans = array_dot(self::trans);
        }

        return self::getData($trans, $key, $replaces);
    }

    /**
     * @param array $base
     * @param string $key
     * @param array $replaces
     * @return mixed|string
     */
    private static function getData(array $base, string $key, array $replaces) {
        if(!empty($replaces)){
            $replace_keys = [];
            $replace_values = [];

            foreach ($replaces as $replace_key => $value){
                $replace_keys[] = '{{' . $replace_key . '}}';
                $replace_values[] = $value;
            }

            return str_replace($replace_keys, $replace_values, $base[$key]);
        }else{
            return $base[$key] ?? null;
        }
    }
